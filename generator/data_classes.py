"""
Data Classes
"""

from dataclasses import dataclass, asdict, field
from typing import List, Dict, Optional, Any


@dataclass
class Skill:
    """Skill node from the graph"""
    id: str
    name: str
    description: str = ""
    weight: float = 1.0
    children: List[str] = field(default_factory=list)
    parent: Optional[str] = None
    
    def __hash__(self):
        return hash(self.id)


@dataclass
class SkillTree:
    """Inverted tree: main skill at bottom, auxiliaries above"""
    main_skill: Skill
    auxiliary_skills: List[Skill]
    dependencies: Dict[str, List[str]] = field(default_factory=dict)


# =============================================================================
# File Profile Types
# =============================================================================

@dataclass
class FileProfile:
    """
    Standardized profile for any file type.
    Generated by AutoProfiler, consumed by downstream pipeline.
    """
    path: str                       # relative path within domain directory
    file_type: str                  # "csv", "excel", "json", "yaml", "html",
                                    # "markdown", "tex", "text", "log",
                                    # "sqlite", "numpy", "netcdf", "geo",
                                    # "unknown"
    
    # Structure info (type-specific)
    structure: Dict[str, Any] = field(default_factory=dict)
    
    # Read parameters (how to load this file)
    read_params: Dict[str, Any] = field(default_factory=dict)
    
    # Content summary
    row_count: Optional[int] = None
    sample: str = ""
    content_summary: str = ""
    
    # Optional: DataFrame representation (for CSV/Excel that can be loaded as tabular)
    dataframe: Optional[Any] = None
    
    # Profiling status
    error: Optional[str] = None


# =============================================================================
# Dataset Containers (updated)
# =============================================================================

@dataclass
class DatasetFile:
    """A single dataset file"""
    name: str
    file_path: str
    profile: Optional[FileProfile] = None
    
    def __str__(self):
        if self.profile:
            return f"{self.name} ({self.profile.file_type}, {self.profile.row_count or '?'} rows)"
        return f"{self.name}"


@dataclass
class DomainDataset:
    """A domain containing multiple dataset files"""
    domain: str
    files: List[DatasetFile]
    def __str__(self):
        return f"{self.domain}: {len(self.files)} files"


# =============================================================================
# Solution & Evaluation (unchanged)
# =============================================================================

@dataclass
class SolutionStep:
    """A single step in the solution"""
    step_number: int
    skill: Skill
    description: str
    code_snippet: str = ""
    input_data: str = ""
    output_description: str = ""
    expected_output_type: str = "dataframe"
    dependent_step_ids: List[int] = field(default_factory=list)


@dataclass
class SolutionPath:
    """Complete solution path"""
    steps: List[SolutionStep]
    main_skill: Skill


@dataclass
class EvalCase:
    """Final evaluation case output"""
    question: str
    data_sources: List[str]       # 数据文件在数据集中的相对路径
    skills: List[str]             # 解决问题必需的技能 (aux skills + main skill，main skill 在最后)
    domain: str
    pipeline: List[Dict[str, Any]]  # 参考解决问题步骤
    synthesis_time_seconds: float = 0.0
    token_usage: Dict[str, int] = field(default_factory=lambda: {
        "prompt_tokens": 0, "completion_tokens": 0, "total_tokens": 0
    })
    
    def to_dict(self) -> Dict:
        return asdict(self)
